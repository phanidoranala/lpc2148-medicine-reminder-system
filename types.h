//types.h

#ifndef TYPES_H              // check if TYPES_H is already defined
#define TYPES_H              // define TYPES_H to prevent multiple inclusion

typedef unsigned char u8;   // define u8 as unsigned 8-bit integer (0 to 255)
typedef char s8;            // define s8 as signed 8-bit integer (-128 to 127)
typedef unsigned int u32;   // define u32 as unsigned 32-bit integer
typedef int s32;            // define s32 as signed 32-bit integer
typedef unsigned short int u16; // define u16 as unsigned 16-bit integer
typedef signed short int s16;   // define s16 as signed 16-bit integer
typedef float f32;          // define f32 as 32-bit floating point number
typedef double d64;         // define d64 as 64-bit floating point number

#endif                      // end of header guard

